//// Metodos ////

// sqrt(): Devuelve la raiz cuadrada positiva de un numero/

var raizCuadrada = Math.sqrt(4);
console.log(raizCuadrada);

// cbrt(): Devuelve la raiz cubica de un numero.

var raizCubica = Math.cbrt(4);
console.log(raizCubica);

// max(): Devuelve el mayor de cero o mas numeros.

var numeros = Math.max(1, 2, 39, 21, 544, 221, 31, 3, 44,)
console.log(numeros);

// min(): Devuelve el mas pequeño de cero o mas numeros.

var numeros = Math.min(1, 2, 39, 21, 544, 221, 31, 3, 44,)
console.log(numeros);

// random(): Devuelve un numero pseudo-aleatorio entre o y 1.

var random = Math.random()
console.log(random);

// round(): Devuelve el valor de un numero redondeado al numero entero mas cercano.

var num = Math.round(1.5)
console.log(num);

// fround(): Devuelve la representacion flotante de precision simple mas cercana de un numero.

console.log(Math.fround(2.314141343143143))

// floor(): Devuelve el mayor entero menor que o igual a un numero.

var numero = Math.floor(2.3);
console.log(numero);

// ceil(): Retorna el entero más cercano mayor o igual a un número dado.

var numero = Math.ceil(2.3);
console.log(numero);

// trunc(): Devuelve la parte entera del numero x, la eliminacion de los digitos fraccionarios.

console.log(Math.trunc(10.99))

//// Propiedades ////

// PI: Radio de la circunferencia de un circulo respectp a su diametro.

console.log(Math.PI);

// SQRT1_2: Raiz cuadrada de 1/2; equivalente 1, sobre la raiz.

console.log(Math.SQRT1_2);

// SQRT2: Raiz cuadrada de 2, aproximadamente 1.414.

console.log(Math.SQRT2);

// E: Costante de Euler, la base de los logaritmos naturales.

console.log(Math.E);

// LN2: Logaritmo natural de 2, aproximadamente 0.693.

console.log(Math.LN2);

// LN10: Logaritmo natural de 10, aproximadamente 2.303.

console.log(Math.LN10);

// LOG2E: Logaritmo de E con base 2, aproximadamente 1,443.

console.log(Math.LOG2E);

// LOG10E: Logaritmo de E con base 10, aproximadambente 0.434

console.log(Math.LOG10E);